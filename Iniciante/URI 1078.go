package main
 
import (
    "fmt"
)
 
func main() {
    var n int

    fmt.Scanf("%d", &n)
    fmt.Printf("1 x %d = %d\n", n, (n * 1))
    fmt.Printf("2 x %d = %d\n", n, (n * 2))
    fmt.Printf("3 x %d = %d\n", n, (n * 3))
    fmt.Printf("4 x %d = %d\n", n, (n * 4))
    fmt.Printf("5 x %d = %d\n", n, (n * 5))
    fmt.Printf("6 x %d = %d\n", n, (n * 6))
    fmt.Printf("7 x %d = %d\n", n, (n * 7))
    fmt.Printf("8 x %d = %d\n", n, (n * 8))
    fmt.Printf("9 x %d = %d\n", n, (n * 9))
    fmt.Printf("10 x %d = %d\n", n, (n * 10))
}